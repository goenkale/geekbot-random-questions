//This is a workaround for an issue I had where dispatching the "input" event for the question input box was being ignored which
//caused the form validation to think there was no text on the last box and keep the "Add Question" button disabled
//See more info about the workaround here: https://github.com/facebook/react/issues/10135#issuecomment-314441175
function setNativeValue(element, value) {
    const valueSetter = Object.getOwnPropertyDescriptor(element, 'value').set;
    const prototype = Object.getPrototypeOf(element);
    const prototypeValueSetter = Object.getOwnPropertyDescriptor(prototype, 'value').set;

    if (valueSetter && valueSetter !== prototypeValueSetter) {
        prototypeValueSetter.call(element, value);
    } else {
        valueSetter.call(element, value);
    }
}

const inputEvent = new Event('input', { bubbles: true });
const changeEvent = new Event('change');
newQuestionsText.forEach((newQuestionText, index) => {
    document.querySelector('.c-drawer-content .gb-button--full-width').click();
    let geekbotQuestions = document.querySelectorAll('.c-drawer-content input.gb-input__input');
    let location = geekbotQuestions.length - 1;
    if (index === 0 || index === 1) {
        location = index;
    }
    setNativeValue(geekbotQuestions[location], newQuestionText);
    geekbotQuestions[location].dispatchEvent(inputEvent);
    geekbotQuestions[location].dispatchEvent(changeEvent);
});